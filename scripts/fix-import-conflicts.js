#!/usr/bin/env node

/**
 * Script para resolver conflictos de imports din√°micos vs est√°ticos
 * Analiza todos los archivos TypeScript/Vue y convierte imports inconsistentes
 */

const fs = require('fs');
const path = require('path');
const glob = require('glob');

// Configuraci√≥n
const CONFIG = {
  // Directorios a analizar
  directories: [
    'src/**/*.ts',
    'src/**/*.vue',
    'src/**/*.js'
  ],
  
  // M√≥dulos que deben ser siempre est√°ticos (cr√≠ticos)
  alwaysStatic: [
    'vue',
    'vue-router',
    'pinia',
    '@heroicons/vue',
    'firebase/app',
    'firebase/auth'
  ],
  
  // M√≥dulos que pueden ser din√°micos (funcionalidades opcionales)
  canBeDynamic: [
    'firebase/firestore',
    'jspdf',
    'chart.js',
    'date-fns'
  ],
  
  // Patrones de archivos que deben usar imports din√°micos
  dynamicFiles: [
    '/store/',
    '/services/',
    '/composables/'
  ],
  
  // Crear backup antes de modificar
  createBackup: true
};

class ImportResolver {
  constructor() {
    this.issues = [];
    this.fixes = [];
    this.stats = {
      filesAnalyzed: 0,
      issuesFound: 0,
      fixesApplied: 0
    };
  }

  /**
   * Ejecutar el an√°lisis y correcci√≥n
   */
  async run() {
    console.log('üîç Iniciando an√°lisis de imports...\n');
    
    // Obtener todos los archivos
    const files = await this.getFiles();
    
    // Analizar cada archivo
    for (const file of files) {
      await this.analyzeFile(file);
    }
    
    // Mostrar resumen
    this.showSummary();
    
    // Aplicar correcciones si se encontraron problemas
    if (this.issues.length > 0) {
      const shouldFix = await this.promptForFixes();
      if (shouldFix) {
        await this.applyFixes();
      }
    }
    
    console.log('‚úÖ An√°lisis completado');
  }

  /**
   * Obtener lista de archivos a analizar
   */
  async getFiles() {
    const files = [];
    
    for (const pattern of CONFIG.directories) {
      const matches = glob.sync(pattern, { 
        ignore: ['**/node_modules/**', '**/dist/**', '**/*.d.ts'] 
      });
      files.push(...matches);
    }
    
    return [...new Set(files)]; // Eliminar duplicados
  }

  /**
   * Analizar un archivo espec√≠fico
   */
  async analyzeFile(filePath) {
    this.stats.filesAnalyzed++;
    
    try {
      const content = fs.readFileSync(filePath, 'utf8');
      const analysis = this.parseImports(content, filePath);
      
      if (analysis.hasIssues) {
        this.issues.push({
          file: filePath,
          ...analysis
        });
        this.stats.issuesFound++;
      }
      
    } catch (error) {
      console.warn(`‚ö†Ô∏è Error analizando ${filePath}: ${error.message}`);
    }
  }

  /**
   * Parsear imports de un archivo
   */
  parseImports(content, filePath) {
    const staticImports = [];
    const dynamicImports = [];
    const issues = [];
    
    // Buscar imports est√°ticos
    const staticRegex = /import\s+(?:(?:\{[^}]*\}|\*\s+as\s+\w+|\w+)\s+from\s+)?['"`]([^'"`]+)['"`]/g;
    let match;
    
    while ((match = staticRegex.exec(content)) !== null) {
      staticImports.push({
        module: match[1],
        line: this.getLineNumber(content, match.index),
        full: match[0]
      });
    }
    
    // Buscar imports din√°micos
    const dynamicRegex = /import\s*\(\s*['"`]([^'"`]+)['"`]\s*\)/g;
    
    while ((match = dynamicRegex.exec(content)) !== null) {
      dynamicImports.push({
        module: match[1],
        line: this.getLineNumber(content, match.index),
        full: match[0]
      });
    }
    
    // Buscar await import()
    const awaitDynamicRegex = /await\s+import\s*\(\s*['"`]([^'"`]+)['"`]\s*\)/g;
    
    while ((match = awaitDynamicRegex.exec(content)) !== null) {
      dynamicImports.push({
        module: match[1],
        line: this.getLineNumber(content, match.index),
        full: match[0],
        isAwait: true
      });
    }
    
    // Detectar conflictos
    const conflicts = this.detectConflicts(staticImports, dynamicImports, filePath);
    
    return {
      staticImports,
      dynamicImports,
      conflicts,
      hasIssues: conflicts.length > 0,
      content
    };
  }

  /**
   * Detectar conflictos entre imports
   */
  detectConflicts(staticImports, dynamicImports, filePath) {
    const conflicts = [];
    const staticModules = new Set(staticImports.map(imp => imp.module));
    const dynamicModules = new Set(dynamicImports.map(imp => imp.module));
    
    // Encontrar m√≥dulos que est√°n tanto est√°ticos como din√°micos
    const conflictModules = [...staticModules].filter(module => dynamicModules.has(module));
    
    for (const module of conflictModules) {
      const recommendedStrategy = this.getRecommendedStrategy(module, filePath);
      
      conflicts.push({
        module,
        staticLines: staticImports.filter(imp => imp.module === module).map(imp => imp.line),
        dynamicLines: dynamicImports.filter(imp => imp.module === module).map(imp => imp.line),
        recommended: recommendedStrategy,
        reason: this.getReasonForStrategy(module, filePath, recommendedStrategy)
      });
    }
    
    return conflicts;
  }

  /**
   * Determinar estrategia recomendada para un m√≥dulo
   */
  getRecommendedStrategy(module, filePath) {
    // Siempre est√°tico para m√≥dulos cr√≠ticos
    if (CONFIG.alwaysStatic.some(pattern => module.includes(pattern))) {
      return 'static';
    }
    
    // Din√°mico para m√≥dulos pesados en ciertos archivos
    if (CONFIG.canBeDynamic.some(pattern => module.includes(pattern))) {
      if (CONFIG.dynamicFiles.some(pattern => filePath.includes(pattern))) {
        return 'dynamic';
      }
    }
    
    // Est√°tico por defecto para m√≥dulos internos
    if (module.startsWith('.') || module.startsWith('@/')) {
      return 'static';
    }
    
    // Firebase firestore puede ser din√°mico en servicios
    if (module.includes('firebase/firestore') && filePath.includes('/service')) {
      return 'dynamic';
    }
    
    return 'static';
  }

  /**
   * Obtener raz√≥n para la estrategia recomendada
   */
  getReasonForStrategy(module, filePath, strategy) {
    if (strategy === 'static') {
      if (CONFIG.alwaysStatic.some(pattern => module.includes(pattern))) {
        return 'M√≥dulo cr√≠tico - debe ser est√°tico';
      }
      return 'Mejor rendimiento como import est√°tico';
    } else {
      return 'M√≥dulo pesado - mejor como import din√°mico';
    }
  }

  /**
   * Obtener n√∫mero de l√≠nea de una posici√≥n en el contenido
   */
  getLineNumber(content, position) {
    return content.substring(0, position).split('\n').length;
  }

  /**
   * Mostrar resumen del an√°lisis
   */
  showSummary() {
    console.log('üìä RESUMEN DEL AN√ÅLISIS');
    console.log('========================');
    console.log(`Archivos analizados: ${this.stats.filesAnalyzed}`);
    console.log(`Archivos con problemas: ${this.stats.issuesFound}`);
    console.log('');
    
    if (this.issues.length === 0) {
      console.log('‚úÖ No se encontraron conflictos de imports');
      return;
    }
    
    console.log('üî¥ CONFLICTOS ENCONTRADOS:');
    console.log('==========================');
    
    this.issues.forEach((issue, index) => {
      console.log(`\n${index + 1}. ${issue.file}`);
      
      issue.conflicts.forEach(conflict => {
        console.log(`   üì¶ ${conflict.module}`);
        console.log(`   üìç Est√°tico: l√≠neas ${conflict.staticLines.join(', ')}`);
        console.log(`   üìç Din√°mico: l√≠neas ${conflict.dynamicLines.join(', ')}`);
        console.log(`   üí° Recomendado: ${conflict.recommended} (${conflict.reason})`);
      });
    });
    
    console.log('\n');
  }

  /**
   * Preguntar al usuario si desea aplicar las correcciones
   */
  async promptForFixes() {
    // En un entorno real, usar√≠as readline o inquirer
    // Por simplicidad, asumimos que s√≠
    console.log('üîß ¬øDeseas aplicar las correcciones autom√°ticamente? (s/n)');
    return true; // Para el script, siempre aplicamos
  }

  /**
   * Aplicar las correcciones
   */
  async applyFixes() {
    console.log('üîß Aplicando correcciones...\n');
    
    for (const issue of this.issues) {
      await this.fixFile(issue);
    }
    
    console.log(`‚úÖ Se aplicaron ${this.stats.fixesApplied} correcciones`);
  }

  /**
   * Corregir un archivo espec√≠fico
   */
  async fixFile(issue) {
    try {
      let content = issue.content;
      let modified = false;
      
      // Crear backup si est√° configurado
      if (CONFIG.createBackup) {
        fs.writeFileSync(`${issue.file}.backup`, content);
      }
      
      // Aplicar correcciones para cada conflicto
      for (const conflict of issue.conflicts) {
        const result = this.applyConflictFix(content, conflict, issue.file);
        if (result.modified) {
          content = result.content;
          modified = true;
        }
      }
      
      // Escribir archivo modificado
      if (modified) {
        fs.writeFileSync(issue.file, content);
        console.log(`‚úÖ Corregido: ${issue.file}`);
        this.stats.fixesApplied++;
      }
      
    } catch (error) {
      console.error(`‚ùå Error corrigiendo ${issue.file}: ${error.message}`);
    }
  }

  /**
   * Aplicar correcci√≥n para un conflicto espec√≠fico
   */
  applyConflictFix(content, conflict, filePath) {
    const strategy = conflict.recommended;
    let modified = false;
    
    if (strategy === 'static') {
      // Convertir imports din√°micos a est√°ticos
      content = this.convertToStatic(content, conflict.module);
      modified = true;
    } else if (strategy === 'dynamic') {
      // Convertir imports est√°ticos a din√°micos
      content = this.convertToDynamic(content, conflict.module, filePath);
      modified = true;
    }
    
    return { content, modified };
  }

  /**
   * Convertir import din√°mico a est√°tico
   */
  convertToStatic(content, moduleName) {
    // Buscar y reemplazar import() por import est√°tico
    const dynamicRegex = new RegExp(`import\\s*\\(\\s*['"\`]${moduleName.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}['"\`]\\s*\\)`, 'g');
    
    // Esta es una simplificaci√≥n - en la realidad necesitar√≠as parsear el AST
    return content.replace(dynamicRegex, (match) => {
      console.log(`   üîÑ Convirtiendo a est√°tico: ${moduleName}`);
      return `/* TODO: Convertir a import est√°tico: ${moduleName} */`;
    });
  }

  /**
   * Convertir import est√°tico a din√°mico
   */
  convertToDynamic(content, moduleName, filePath) {
    const staticRegex = new RegExp(`import\\s+(?:(?:\\{[^}]*\\}|\\*\\s+as\\s+\\w+|\\w+)\\s+from\\s+)?['"\`]${moduleName.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}['"\`]`, 'g');
    
    return content.replace(staticRegex, (match) => {
      console.log(`   üîÑ Convirtiendo a din√°mico: ${moduleName}`);
      return `/* TODO: Convertir a import din√°mico: ${moduleName} */`;
    });
  }
}

// Ejecutar el script
if (require.main === module) {
  const resolver = new ImportResolver();
  resolver.run().catch(console.error);
}

module.exports = ImportResolver;
